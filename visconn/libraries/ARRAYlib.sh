array_contains () {
                ############# ############# ############# ############# ############# #############
                #############          Check if an array contains a value             #############
                ############# ############# ############# ############# ############# #############

if [ $# -lt 2 ]; then
    echo $0: "usage: array_contains <value> <array_name>"
    return 1;
fi

local value="$1"
shift
local arr=("$@")
for element in "${arr[@]}"; do
[ "${element}" == "${value}" ] && { echo 1; return 0; }
done
echo 0
};


array_unique () {
                ############# ############# ############# ############# ############# #############
                #############       Return unique elements of an array                #############
                ############# ############# ############# ############# ############# #############

if [ $# -lt 1 ]; then
    echo $0: "usage: array_unique <element1> [<element2> ...]"
    return 1;
fi

local arr=("$@")
local -A seen
local result=()
for element in "${arr[@]}"; do
if [ -z "${seen[$element]+_}" ]; then
seen[$element]=1
result+=("$element")
fi
done
echo "${result[@]}"
};


array_join () {
                ############# ############# ############# ############# ############# #############
                #############          Join array elements with a separator           #############
                ############# ############# ############# ############# ############# #############

if [ $# -lt 2 ]; then
    echo $0: "usage: array_join <separator> <element1> [<element2> ...]"
    return 1;
fi

local sep="$1"
shift
local arr=("$@")
local result=""
for i in "${!arr[@]}"; do
if [ $i -eq 0 ]; then
result="${arr[$i]}"
else
result="${result}${sep}${arr[$i]}"
fi
done
echo "${result}"
};


array_len () {
                ############# ############# ############# ############# ############# #############
                #############          Return the length of an array                  #############
                ############# ############# ############# ############# ############# #############

if [ $# -lt 1 ]; then
    echo $0: "usage: array_len <element1> [<element2> ...]"
    return 1;
fi

local arr=("$@")
echo "${#arr[@]}"
};
